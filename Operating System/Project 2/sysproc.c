int
sys_fork(void)
{
  return fork();
}

int
sys_exit(void)
{
  exit();
  return 0;  // not reached
}

int
sys_wait(void)
{
  return wait();
}

int
sys_kill(void)
{
  int pid;

  if(argint(0, &pid) < 0)
    return -1;
  return kill(pid);
}

int
sys_getpid(void)
{
  return myproc()->pid;
}

int
sys_sbrk(void)
{
  int addr;
  int n;

  if(argint(0, &n) < 0)
    return -1;
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

int
sys_sleep(void)
{
  int n;
  uint ticks0;

  if(argint(0, &n) < 0)
    return -1;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(myproc()->killed){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

// return how many clock tick interrupts have occurred
// since start.
int
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

int
sys_date(void)
{
    struct rtcdate *dt;

    if(argptr(0, (void*)&dt, sizeof(*dt)) < 0) // validate argument
        return -1;

    cmostime(dt); // get time from cmostime
    return 0;
}

int
sys_alarm(void)
{
    int sec;
    if(argint(0, &sec) < 0)
        return -1;
    if (sec <= 0) return -1;
    myproc()->alarmticks = 0; // reset current alarm tick to 0.
    myproc()->alarm_timer = sec * 100; // 1tick = 10ms in xv6, that is 1000ms(1s, unit of argument) equals to 100tick.
    return 0;
}
